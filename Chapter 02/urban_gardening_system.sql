
--creating 3 basic tables area, plant, plantingmap:

create table area
(aname varchar(20),
alocation varchar(20),
atype varchar(20),
asize integer,
primary key (aname));

create table plant
(pname varchar(20),
ptype varchar(20),
pdiameter numeric(4,2),
pmaxheight numeric(4,2),
primary key (pname));

create table plantingmap
(pname varchar(20) references plant,
aname varchar(20) references area,
amount integer,
primary key (aname, pname));

